def classify_intent(
    text: str,
    parse_height_weight=None,
    extra_food_kws: list[str] | None = None,
) -> str:
    """
    Returns the highest-scoring intent name, or 'unknown' if nothing matched.

    parse_height_weight: callable(text) -> (height, weight | None, None)
        若解析出合法身高體重，health 額外加 5 分（強信號）。
    extra_food_kws: 補充食物關鍵字清單（例如 _ALL_FOOD_KEYWORDS），每個 +2 分。
    """
    scores: dict[str, int] = {k: 0 for k in _RULES}

    for intent, rules in _RULES.items():
        for kw, weight in rules:
            if kw in text:
                scores[intent] += weight

    # 身高體重數字組合 → 強烈暗示 health
    if parse_height_weight:
        try:
            h, w = parse_height_weight(text)
            if h and w:
                scores["health"] += 5
        except Exception:
            pass

    # 補充食物關鍵字
    if extra_food_kws:
        for kw in extra_food_kws:
            if kw in text:
                scores["food"] += 2

    best = max(scores, key=scores.get)
    return best if scores[best] > 0 else "unknown"
